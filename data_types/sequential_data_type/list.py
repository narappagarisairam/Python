# List Creation
my_list = [1, 2, 3, 4, 5]

# List Methods
my_list.append(6)        # Adds an item to the end of the list
my_list.extend([7, 8])   # Extends the list by appending elements from another iterable
my_list.insert(2, 2.5)   # Inserts an item at a given position
my_list.remove(2.5)      # Removes the first item from the list with the specified value
my_list.pop(3)           # Removes and returns the item at the given position
my_list.clear()          # Removes all items from the list
my_list.index(3)         # Returns the index of the first item with the specified value
my_list.count(1)         # Returns the number of times the specified value appears in the list
my_list.sort()           # Sorts the items of the list in place
my_list.reverse()        # Reverses the elements of the list in place
my_list.copy()           # Returns a shallow copy of the list

